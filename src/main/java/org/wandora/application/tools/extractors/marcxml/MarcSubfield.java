    private static HashMap<String,String> subfieldNameHash = null;


    public static String getSubfieldName(String field, String subfield) {
        if(subfieldNameHash == null) {
            subfieldNameHash = new HashMap<String,String>();
            for(int i=0; i<subfieldNames.length; i=i+2) {
                String[] subfields = (String[]) subfieldNames[i+1];
                for(int j=0; j<subfields.length; j=j+2) {
                    String hashKey = ((String) subfieldNames[i])+((String) subfields[j]);
                    subfieldNameHash.put(hashKey, (String) subfields[j+1]);
                }
            }
        }
        if(subfieldNameHash.containsKey(field+subfield)) {
            return subfieldNameHash.get(field+subfield) + " ("+subfield+"@"+field+")";
        }
        return subfield+"@"+field;
    }


