    public void progress(double ratio, Abortable.Status status, String message) {
        this.ratio = ratio;
        this.status = status;
        msgLabel.setText("<html><body><p>" + message + "</p></body></html>");
        jProgressBar1.setValue((int)(ratio * 1000));
        
        switch(status) {
            case InProgress:
                break;
            case Success:
                closeBtn.setText("OK");
                break;
            case Failure:
                closeBtn.setText("OK");
                break;
        }
    }
    
    private void closeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_closeBtnActionPerformed
        switch(status) {
            case InProgress:
                if(abortProc != null)
                    abortProc.run();
                break;
            case Success:
                break;
            case Failure:
                break;
        }
        
        this.setVisible(false);
}//GEN-LAST:event_closeBtnActionPerformed
    
