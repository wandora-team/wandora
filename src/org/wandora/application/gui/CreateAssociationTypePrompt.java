    public void addPlayer(Topic role,Topic roleClass) throws TopicMapException {
        T2<GetTopicButton,GetTopicButton> t=t2(new GetTopicButton(role,admin,this),new GetTopicButton(roleClass,admin,this,true));
        roles.add(t);
        refreshPlayersPanel();
    }
    
